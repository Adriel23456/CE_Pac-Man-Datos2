std::vector<Nodo> initializeLevel1(){
    std::vector<Nodo> level1;
    // Copia los valores de level1Data a la matriz level1 de objetos Nodo
    for (int i = 0; i < 7; ++i) {
        for (int j = 0; j < 10; ++j) {
            level1.push_back(Nodo(level1Data[i][j], i+1,j+1));
        }
    }
    return level1;
}

